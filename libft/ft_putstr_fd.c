#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	len;
	int	cont;

	len = ft_strlen(s);
	cont = 0;
	while (cont < len)
	{
		write(fd, &s[cont], 1);
		cont++;
	}
}
